    public void loadTable()
    {
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
         model.setRowCount(0);
        blist=fbase.getAllData();
       
    for(StudentModel b:blist)
    {
        Object[] obja={b.getId(),b.getName(),b.getSemester(),b.getPerformance_result()};
        model.addRow(obja);
    }
         
    }
    public void search(String id){
        List<StudentModel> templist=new ArrayList<StudentModel>();
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
         model.setRowCount(0);
        for(StudentModel modelst:blist)
        {
            if(modelst.getId().equals(id))
            {
                Object[] obja={modelst.getId(),modelst.getName(),modelst.getSemester(),modelst.getPerformance_result()};
        model.addRow(obja);
            }
        }
        
    }
