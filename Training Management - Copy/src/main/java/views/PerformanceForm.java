    public double curriculum(double x)
    {
        if(x==1)
        {
            x=0.25;
        }
        else if(x==2)
        {
            x=0.50;
        }
         else if(x==3)
        {
            x=0.75;
        }
         else if(x>=4)
        {
            x=1;
        }
        return x;
        
    }
